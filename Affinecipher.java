    private void deskripsibuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deskripsibuttonActionPerformed
           try {
        int a = Integer.parseInt(txta.getText());
        int b = Integer.parseInt(txtb.getText());
        String cipherText = txtenkripsi.getText().toUpperCase();

        if (!isValidKey(a)) {
            JOptionPane.showMessageDialog(this, "Nilai a tidak valid untuk mod 26. Harus coprime dengan 26.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int aInverse = modularInverse(a, 26);
        if (aInverse == -1) {
            JOptionPane.showMessageDialog(this, "Tidak ditemukan inverse untuk nilai a.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        StringBuilder plainText = new StringBuilder();
        for (char ch : cipherText.toCharArray()) {
            if (Character.isLetter(ch)) {
                int y = ch - 'A';
                char decryptedChar = (char) (((aInverse * (y - b + 26)) % 26) + 'A');
                plainText.append(decryptedChar);
            } else {
                plainText.append(ch); // Jika bukan huruf, tetap tambahkan ke hasil
            }
        }

        txtdeskripsi.setText(plainText.toString());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan nilai a dan b yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (HeadlessException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
         }
    }//GEN-LAST:event_deskripsibuttonActionPerformed

    private void enskripsibuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enskripsibuttonActionPerformed
        try {
        int a = Integer.parseInt(txta.getText());
        int b = Integer.parseInt(txtb.getText());
        String plaintext = txtplaint.getText().toUpperCase();

        if (!isValidKey(a)) {
            JOptionPane.showMessageDialog(this, "Nilai a tidak valid untuk mod 26. Harus coprime dengan 26.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        StringBuilder cipherText = new StringBuilder();
        for (char ch : plaintext.toCharArray()) {
            if (Character.isLetter(ch)) {
                int x = ch - 'A';
                char encryptedChar = (char) (((a * x + b) % 26) + 'A');
                cipherText.append(encryptedChar);
            } else {
                cipherText.append(ch); // Jika bukan huruf, tetap tambahkan ke hasil
            }
        }

        txtenkripsi.setText(cipherText.toString());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Masukkan nilai a dan b yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (HeadlessException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_enskripsibuttonActionPerformed


    private boolean isValidKey(int a) {
       return gcd(a, 26) == 1;  // Memeriksa apakah a coprime dengan 26
    }

    private int modularInverse(int a, int i) {
       for (int x = 1; x < i; x++) {
        if ((a * x) % i == 1) {
            return x;  // Mengembalikan invers modular
        }
    }
    return -1;  // Jika tidak ditemukan invers modular
   
    }

    private int gcd(int a, int b) {
        while (b != 0) {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
    }
